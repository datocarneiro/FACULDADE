/*SINTAXE PARA CRIAR FUNÇÕES

<tipo de retorno> <nome> (<parâmetros>) 
{
<Comandos da função>
 <Retorno> (não obrigatório)
}

* <tipo de retorno> – Obrigatório. Esse parâmetro indica qual o tipo de valor que a função vai retornar.
* <nome> – Obrigatório. Parâmetro que especifica o nome que identificará a função. 
* <parênteses depois do nome> – Obrigatório. Toda função ou
procedimento sempre terá o nome acompanhado de parênteses.
Por exemplo: main(), printf(), somar() etc.
* <comandos da função> – Opcional. Veremos na próxima seção.
* <retorno> – Quando o tipo de retorno for void esse parâmetro não
precisa ser usado, porém, quando não for void é obrigatório. 
*/